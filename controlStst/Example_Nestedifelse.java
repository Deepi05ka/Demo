package com.controlStst;

public class Example_Nestedifelse {

	public static void main(String[] args) {
		int a=70;
		if(a<70) {
			System.out.println("not equal");
		if(a>70)
			System.out.println("not equal");
		else
			System.out.println("equal");
		}
		else
			System.out.println("equal");
	}

}
