package com.Sample_Package;

public class SwappingWith {

	public static void main(String[] args) {
		int a=10,b=20;
        int t;
        System.out.println("Before Swapping"+" "+a+" "+b);
        t=a;
        a=b;
        b=t;
        System.out.println("After Swapping" +" "+a+" "+b);

	}

}
